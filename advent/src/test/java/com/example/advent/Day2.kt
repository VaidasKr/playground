    fun runTestWithInput(input: String): Int {
        var sum = 0
        input.split("\n").forEach {
            val (opponent, you) = it.trim().split(" ")
            sum += you.onOption(
                "X",
                "Y",
                "Z",
                onRock = {
                    1 + opponent.onOption(
                        "A",
                        "B",
                        "C",
                        onRock = { 3 },
                        onPaper = { 0 },
                        onScissors = { 6 }
                    )
                },
                onPaper = {
                    2 + opponent.onOption(
                        "A",
                        "B",
                        "C",
                        onRock = { 6 },
                        onPaper = { 3 },
                        onScissors = { 0 }
                    )
                },
                onScissors = {
                    3 + opponent.onOption(
                        "A",
                        "B",
                        "C",
                        onRock = { 0 },
                        onPaper = { 6 },
                        onScissors = { 3 }
                    )
                }
            )
        }
        return sum
    }

    // x-lose, y->draw , z-> win
    fun runTestWithInputPart2(input: String): Int {
        var sum = 0
        input.split("\n").forEach {
            val (opponent, you) = it.trim().split(" ")
            sum += opponent.onOption(
                "A",
                "B",
                "C",
                onRock = {
                    you.onOption(
                        "X",
                        "Y",
                        "Z",
                        onRock = { /* lose */ 3 },
                        onPaper = { /* draw */ 1 + 3 },
                        onScissors = { /* win */ 2 + 6 }
                    )
                },
                onPaper = {
                    you.onOption(
                        "X",
                        "Y",
                        "Z",
                        onRock = { /* lose */ 1 },
                        onPaper = { /* draw */ 2 + 3 },
                        onScissors = { /* win */ 3 + 6 }
                    )
                },
                onScissors = {
                    you.onOption(
                        "X",
                        "Y",
                        "Z",
                        onRock = { /* lose */ 2 },
                        onPaper = { /* draw */ 3 + 3 },
                        onScissors = { /* win */ 1 + 6 }
                    )
                }
            )
        }
        return sum
    }

    private inline fun String.onOption(
        rock: String,
        paper: String,
        scissors: String,
        onRock: () -> Int,
        onPaper: () -> Int,
        onScissors: () -> Int
    ): Int = when (this) {
        rock -> onRock()
        paper -> onPaper()
        scissors -> onScissors()
        else -> throw RuntimeException("bad input ($this)")
    }

